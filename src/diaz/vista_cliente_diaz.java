    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
       try {
        // Leer valores del formulario
        double densidad = Double.parseDouble(txtDensidad.getText());
        double volumen = Double.parseDouble(txtVolumen.getText());

        // Crear instancia del servicio web
        diaz.RecuDiazWebOperaciones9A_Service servicio = new diaz.RecuDiazWebOperaciones9A_Service();
        diaz.RecuDiazWebOperaciones9A port = servicio.getRecuDiazWebOperaciones9APort();

        // Llamar al mÃ©todo del Web Service
        double masa = port.calcularMasa(densidad, volumen);

        // Mostrar resultado
        txtMasa.setText(String.format("%.2f gramos", masa));

    } catch (NumberFormatException e) {
        txtMasa.setText("Error: valores invÃ¡lidos");
    } catch (Exception e) {
        e.printStackTrace(); // para ver detalles en consola
        txtMasa.setText("Error al conectar al servicio");
    }
        // ðŸ‘ˆ Esto muestra el error real en la consola de NetBeans


    }//GEN-LAST:event_btnCalcularActionPerformed

